function getWeaponFromRow(tr) {
   let aura = {};
   for (let item of gear.weapon)
      if (item.ID == tr.data('id') && item.Name == tr.data('name')) {
         aura.str = parseInt(item.Str || 0);
         aura.agi = parseInt(item.Agi || 0);
         aura.ap = parseInt(item.AP || 0);
         aura.hit = parseInt(item.Hit || 0);
         aura.crit = parseInt(item.Crit || 0);
         aura.minhit = parseInt(item["Min Hit"] || 0);
         aura.maxhit = parseInt(item["Max Hit"] || 0);
         aura.speed = parseFloat(item.Speed || 0);
         aura.type = WEAPONTYPE[item.Type.toUpperCase()];
         let skill = parseInt(item.Skill || 0);
         if (skill > 0) aura['skill_' + aura.type] = skill;
         aura.slot = tr.parents('table').data('type');
         aura.physdmg = parseInt(item.PhysDmg || 0);
         aura.magicdmg = parseInt(item.MagicDmg || 0);
         aura.procextra = parseInt(item.ProcExtra || 0);
         aura.ppm = parseFloat(tr.find('td').eq(13).text() || 0);
         aura.procspell = (item.ProcSpell || '');
         break;
      }
   return aura;
}

function getEnchantFromRow(tr) {
   let aura = {};
   for (let item of gear.enchant)
      if (item.ID == tr.data('id') && item.Slot == tr.data('slot')) {
         aura.slot = item.Slot;
         aura.str = parseInt(item.Str || 0);
         aura.agi = parseInt(item.Agi || 0);
         aura.haste = parseInt(item.Haste || 0);
         aura.crit = parseInt(item.Crit || 0);
         aura.dmg = parseInt(item.Dmg || 0);
         aura.ap = parseInt(item.AP || 0);
         aura.ppm = parseFloat(item.PPM || 0);
         aura.magicdmg = parseInt(item.MagicDmg || 0);
         aura.procspell = (item.ProcSpell || '');
         aura.enchant = true;
         break;
      }
   return aura;
}

function getGearFromRow(tr) {
   let aura = {};
   let slot = tr.parents('table').data('type');
   for (let item of gear[slot])
      if (item.ID == tr.data('id')) {
         aura.str = parseInt(item.Str || 0);
         aura.agi = parseInt(item.Agi || 0);
         aura.ap = parseInt(item.AP || 0);
         aura.crit = parseInt(item.Crit || 0);
         aura.hit = parseInt(item.Hit || 0);
         aura.slot = slot;
         let skill = parseInt(item.Skill || 0);
         if (skill > 0) {
            aura.type = WEAPONTYPE[item.Type.toUpperCase()];
            aura['skill_' + aura.type] = skill;
            if (item.Type == 'Varied') {
               aura['skill_1'] = skill;
               aura['skill_2'] = skill;
               aura['skill_3'] = skill;
            }
         }
         break;
      }
   return aura;
}

function getAuraFromRow(tr) {
   let type = tr.parents('table').data('type');
   if (type == 'mainhand' || type == 'offhand')
      return getWeaponFromRow(tr);
   else if (type == 'enchant')
      return getEnchantFromRow(tr);
   else
      return getGearFromRow(tr);
}

function addGearToPlayer(player, aura) {
   for (let prop in player.base)
      player.base[prop] += aura[prop] || 0;

   if (aura.slot == 'mainhand') {
      player.mh = new Weapon(player, aura.speed, aura.minhit, aura.maxhit, aura.type, false);
      if (aura.ppm) {
         player.mh.proc1 = {};
         player.mh.proc1.chance = player.mh.speed * aura.ppm / 0.6;
         if (aura.physdmg) player.mh.proc1.physdmg = aura.physdmg;
         if (aura.magicdmg) player.mh.proc1.magicdmg = aura.magicdmg;
         if (aura.procextra) player.mh.proc1.extra = aura.procextra;
         if (aura.procspell) {
            player.auras[aura.procspell.toLowerCase()] = eval('new ' + aura.procspell + '(player)');
            player.mh.proc1.spell = player.auras[aura.procspell.toLowerCase()];
         }
      }
   }

   if (aura.slot == 'offhand') {
      player.oh = new Weapon(player, aura.speed, aura.minhit, aura.maxhit, aura.type, true);
      if (aura.ppm) {
         player.oh.proc1 = {};
         player.oh.proc1.chance = player.oh.speed * aura.ppm / 0.6;
         if (aura.physdmg) player.oh.proc1.physdmg = aura.physdmg;
         if (aura.magicdmg) player.oh.proc1.magicdmg = aura.magicdmg;
         if (aura.procextra) player.oh.proc1.extra = aura.procextra;
         if (aura.procspell) {
            player.auras[aura.procspell.toLowerCase()] = eval('new ' + aura.procspell + '(player)');
            player.oh.proc1.spell = player.auras[aura.procspell.toLowerCase()];
         }
      }
   }
}

function addEnchantToPlayer(player, aura) {
   if (aura.slot.indexOf('Temporary') == -1) {
      for (let prop in player.base) {
         if (prop == 'haste')
            player.base.haste /= (1 + aura.haste / 100) || 1;
         else
            player.base[prop] += aura[prop] || 0;
      }
   }

   if (aura.slot.indexOf('Main Hand') >= 0) {
      player.mh.crit += aura.crit;
      player.mh.bonusdmg += aura.dmg;
      player.base.ap += aura.ap;
      if (aura.ppm) {
         player.mh.proc2 = {};
         player.mh.proc2.chance = player.mh.speed * aura.ppm / 0.6;
         player.mh.proc2.magicdmg = aura.magicdmg;
         if (aura.procspell) {
            player.auras.crusader1 = new Crusader(player);
            player.mh.proc2.spell = player.auras.crusader1;
         }
      }
   }
   if (aura.slot.indexOf('Off Hand') >= 0) {
      player.oh.crit += aura.crit;
      player.oh.bonusdmg += aura.dmg;
      player.base.ap += aura.ap;
      if (aura.ppm) {
         player.oh.proc2 = {};
         player.oh.proc2.chance = player.oh.speed * aura.ppm / 0.6;
         player.oh.proc2.magicdmg = aura.magicdmg;
         if (aura.procspell) {
            player.auras.crusader2 = new Crusader(player);
            player.oh.proc2.spell = player.auras.crusader2;
         }
      }
   }
}

function buildPlayer(testgear) {

   let player = new Player();

   // Talents
   $('.talent').each(function () {
      let count = parseInt($(this).attr('data-count'));
      let talent = getTalent($(this));
      $.extend(player.talents, talent.aura(count));
   });

   // Gear
   $('table.gear').each(function () {
      let slot = $(this).data('type');
      if (slot == 'enchant') return;
      let tr = $(this).find('tbody tr.active');

      let aura = {};
      if (testgear && testgear.slot == slot) aura = testgear;
      else if (tr.length) aura = getAuraFromRow(tr);

      addGearToPlayer(player, aura);
   });

   if (!player.mh || !player.oh) return player;

   // Enchants
   let testgearadded = false;
   $('table.gear[data-type="enchant"] tbody tr.active').each(function () {
      let tr = $(this);
      let aura = getAuraFromRow(tr);
      if (testgear && testgear.enchant && testgear.slot == aura.slot) {
         aura = testgear;
         testgearadded = true;
      }
      addEnchantToPlayer(player, aura);
   });
   if (testgear && testgear.enchant && !testgearadded)
      addEnchantToPlayer(player, testgear);

   // Buffs
   $('.buff.active').each(function () {
      let apbonus = 0;
      if ($(this).data('group') == "battleshout")
         apbonus = ~~($(this).data('ap') * player.talents.impbattleshout);
      if ($(this).data('spell')) {
         player.auras[$(this).data('spell').toLowerCase()] = eval('new ' + $(this).data('spell') + '(player);');
      }

      player.base.ap += ($(this).data('ap') || 0) + apbonus;
      player.base.agi += $(this).data('agi') || 0;
      player.base.str += $(this).data('str') || 0;
      player.base.crit += $(this).data('crit') || 0;
      player.base.agimod *= (1 + $(this).data('agimod') / 100) || 1;
      player.base.strmod *= (1 + $(this).data('strmod') / 100) || 1;
      player.base.dmgmod *= (1 + $(this).data('dmgmod') / 100) || 1;
      player.base.haste /= (1 + $(this).data('haste') / 100) || 1;
   });

   // Settings
   let settings = {};
   $('.settings input').each(function () {
      settings[$(this).attr('name')] = parseInt($(this).val());
   });
   $('.race.active').each(function () {
      let r = $(this);
      player.base.aprace = (r.data('ap') || 0);
      player.base.ap += (r.data('ap') || 0);
      player.base.str += (r.data('str') || 0);
      player.base.agi += (r.data('agi') || 0);
      player.base.skill_0 += (r.data('skill_0') || 0);
      player.base.skill_1 += (r.data('skill_1') || 0);
      player.base.skill_2 += (r.data('skill_2') || 0);
      player.base.skill_3 += (r.data('skill_3') || 0);
   });
   $('.spell.active').each(function () {
      let name = $(this).find('img').attr('alt');
      let lname = name.toLowerCase();
      let type = $(this).data('type');
      if (type == 'buff') player.auras[lname] = eval(`new ${name}(player)`);
      else player.spells[lname] = eval(`new ${name}(player)`);
   });
   player.target.armor = settings.armor;
   if (player.talents.flurry) player.auras.flurry = new Flurry(player);
   if (player.spells.overpower) player.auras.battlestance = new BattleStance(player);

   player.update();
   return player;
}