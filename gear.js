function gearEvents() {

   $('.sources li').click(function () {
      $(this).toggleClass('active');
      filterGear();
      updateSession();
   });
   $('.phases li').click(function () {
      $(this).toggleClass('active');
      let sources = $(this).data('sources').split(',');
      let show = $(this).hasClass('active');
      for (let source of sources) {
         if (show) $('.sources [data-id="' + source + '"]').addClass('active');
         else $('.sources [data-id="' + source + '"]').removeClass('active');
      }
      filterGear();
      updateSession();
   });

   $("table.gear").tablesorter({
      widthFixed: true,
      sortList: [[12, 1]]
   });

   $('table.gear tbody td:not(.ppm)').click(function () {
      let tr = $(this).parent();
      let type = $(this).parents('table').data('type');
      tr.toggleClass('active');

      if (type == "enchant") {
         let aura = getEnchantFromRow(tr);
         tr.siblings().each(function () {
            let sibling = getEnchantFromRow($(this));
            if (sibling.slot == aura.slot)
               $(this).removeClass('active');
         });
      }
      else {
         tr.siblings().removeClass('active');
      }
      updatePanel();
      updateSession();
   });
}

function filterGear() {
   $('table.gear').each(function () {
      let type = $(this).data('type');
      if (type == 'mainhand' || type == 'offhand') type = 'weapon';
      $(this).find('tbody tr').each(function () {
         let name = $(this).find('td:first-of-type').text();
         let item = {};
         for (let i of gear[type])
            if (i.Name == name)
               item = i;

         if (item.Source == 'Lethon' || item.Source == 'Emeriss' || item.Source == 'Kazzak' || item.Source == 'Azuregos' || item.Source == 'Ysondre' || item.Source == 'Taerar')
            item.Source = 'worldboss';

         let visible = true;
         if (item.Phase && !$('.phases [data-id="' + item.Phase + '"]').hasClass('active'))
            visible = false;
         if (item.Source && !$('.sources [data-id="' + item.Source.toLowerCase() + '"]').hasClass('active'))
            visible = false;

         if (visible) $(this).removeClass('hidden');
         else $(this).addClass('hidden').removeClass('active');
      });
   });
}

